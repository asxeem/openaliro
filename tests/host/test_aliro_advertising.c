#include "test.h"

#include "advertising_core.h"
#include "ccc_kdf.h"

#include <stdint.h>
#include <string.h>

struct dynamic_tag_vector {
	uint8_t key[16];
	uint8_t address_le[6];
	uint32_t expiry;
	uint8_t plaintext[16];
	uint8_t ciphertext[16];
	uint8_t tag[7];
};

void test_aliro_advertising(void)
{
	/* Aliro Specification 1.0, Appendix 20. Addresses are reversed here to
	 * match the bt_addr_t byte order passed by the nRF application. */
	static const struct dynamic_tag_vector vectors[] = {
		{
			.key = { 0xf5, 0xb1, 0x65, 0x22, 0x4a, 0x58, 0xb7, 0x91,
				 0xdf, 0x6a, 0xf1, 0xd8, 0x30, 0x3e, 0x61, 0xcd },
			.address_le = { 0x10, 0x27, 0xc3, 0x86, 0xbb, 0xc4 },
			.expiry = 0x7a4b8500,
			.plaintext = { 0, 0, 0, 0, 0, 0, 0xc4, 0xbb, 0x86, 0xc3, 0x27, 0x10,
				       0x7a, 0x4b, 0x85, 0x00 },
			.ciphertext = { 0x7b, 0x7f, 0x4a, 0x82, 0x55, 0x79, 0x90, 0x29,
					0xf0, 0x01, 0x4c, 0x3a, 0x77, 0x26, 0xb8, 0xdf },
			.tag = { 0x7b, 0x7f, 0x4a, 0x82, 0x55, 0x79, 0x90 },
		},
		{
			.key = { 0x3c, 0x34, 0x4c, 0x41, 0x89, 0xeb, 0x2f, 0x1e,
				 0x7b, 0xd5, 0xd4, 0x7e, 0x44, 0x6f, 0xce, 0xc2 },
			.address_le = { 0x78, 0xe5, 0x73, 0x11, 0xd8, 0xa3 },
			.expiry = 0x7a4b8500,
			.plaintext = { 0, 0, 0, 0, 0, 0, 0xa3, 0xd8, 0x11, 0x73, 0xe5, 0x78,
				       0x7a, 0x4b, 0x85, 0x00 },
			.ciphertext = { 0xef, 0x67, 0xe4, 0x68, 0x1a, 0x77, 0x83, 0x10,
					0x39, 0x07, 0xed, 0x99, 0x90, 0x8c, 0xc2, 0xeb },
			.tag = { 0xef, 0x67, 0xe4, 0x68, 0x1a, 0x77, 0x83 },
		},
		{
			.key = { 0x1b, 0xcc, 0xce, 0xa6, 0x96, 0x76, 0x2e, 0x61,
				 0x16, 0xc6, 0xe9, 0xc9, 0x2d, 0x99, 0xbf, 0x35 },
			.address_le = { 0x7c, 0xe4, 0x18, 0x07, 0x2e, 0x8c },
			.expiry = 0x7a4b8500,
			.plaintext = { 0, 0, 0, 0, 0, 0, 0x8c, 0x2e, 0x07, 0x18, 0xe4, 0x7c,
				       0x7a, 0x4b, 0x85, 0x00 },
			.ciphertext = { 0xd4, 0xdd, 0x12, 0xa4, 0x50, 0x37, 0xba, 0x80,
					0xdd, 0x8e, 0xe8, 0xd1, 0x36, 0x12, 0x21, 0x06 },
			.tag = { 0xd4, 0xdd, 0x12, 0xa4, 0x50, 0x37, 0xba },
		},
	};

	for (unsigned int i = 0; i < sizeof(vectors) / sizeof(vectors[0]); ++i) {
		uint8_t plaintext[16];
		uint8_t ciphertext[16];
		uint8_t tag[7];

		woz_aliro_dynamic_tag_input(vectors[i].address_le, vectors[i].expiry, plaintext);
		T_OK("dynamic-tag plaintext", memcmp(plaintext, vectors[i].plaintext, sizeof(plaintext)) == 0);
		T_EQ("dynamic-tag AES", crypto_aes_ecb_encrypt(vectors[i].key, 128, plaintext, ciphertext), 0);
		T_OK("dynamic-tag ciphertext", memcmp(ciphertext, vectors[i].ciphertext, sizeof(ciphertext)) == 0);
		woz_aliro_dynamic_tag_from_ciphertext(ciphertext, tag);
		T_OK("dynamic-tag truncation", memcmp(tag, vectors[i].tag, sizeof(tag)) == 0);
	}
}
